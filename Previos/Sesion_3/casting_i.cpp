#include <iostream> 
using namespace std;

int main() {
    // Se le asigna un valor tipo int
    int num_int = 9;

    // se declara una variable tipo double 
    double num_double;

    // pasa de int a double
    num_double = num_int;
    cout << "num_int = " << num_int << endl;
    cout << "num_double = " <<num_double << endl;
    return 0;
}