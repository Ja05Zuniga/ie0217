# Diferentes tipos de tuplas

# Tupla vacía
my_tuple = ()
print(my_tuple)
#Fin

# Tupla con enteros
my_tuple = (1, 2, 3)
print(my_tuple)
#Fin

# Tupla con tipos de datos mixtos
my_tuple = (1, "Hola", 3.4)
print(my_tuple)
#Fin

# Tupla anidada
my_tuple = ("ratón", [8, 4, 6], (1, 2, 3))
print(my_tuple)